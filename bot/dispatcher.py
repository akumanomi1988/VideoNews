"""
Centralized dispatcher for Telegram bot handlers.

This module initializes and collects all command, callback query, and message
handlers for the bot application. These lists of handlers are then imported
by the main application module (`bot.main`) to be registered with the
Telegram Application (Dispatcher).

Handlers are imported from the `bot.handlers` package.
"""
from telegram.ext import CommandHandler, CallbackQueryHandler, MessageHandler, filters

# Import handlers from bot.handlers
import bot.handlers.news_handler as news_handler
import bot.handlers.settings_handler as settings_handler
import bot.handlers.utility_handler as utility_handler # Added utility_handler

# Command Handlers
command_handlers = [
    # Utility Handlers
    CommandHandler("help", utility_handler.help_command_handler),

    # Settings Handlers
    CommandHandler("settings", settings_handler.configure_setting),
    CommandHandler("show_settings", settings_handler.list_settings),

    # News Handlers
    CommandHandler("news_category", news_handler.show_category_selection),
    CommandHandler("topic_shortnews", news_handler.short_news_topic),
    CommandHandler("detailed_news", news_handler.long_news), # Alias for long_news_flow -> show_category_selection
    CommandHandler("topic_longnews", news_handler.long_news_topic),
    CommandHandler("headless", news_handler.headless),
]

# Callback Query Handlers
callback_query_handlers = [
    # Settings Handlers
    # Pattern for settings_category_selection_handler (handles selection of a setting category like 'telegram', 'newsapi')
    # The original pattern in telegram_bot.py for the unified category_selection_handler was:
    # pattern='^(business|entertainment|general|health|science|sports|technology|cancel_config)$'
    # This was used when settings categories were among the news categories.
    # In bot/main.py, I used a more generic pattern: '^([a-zA-Z_]+|cancel_config)$'
    # The subtask specifies: '^(business|entertainment|general|health|science|sports|technology|cancel_config)$'
    # Given settings_handler.py's configure_setting lists settings.keys(), a generic pattern seems more appropriate
    # if settings keys are not always from the news categories.
    # However, I will follow the subtask's specified pattern.
    CallbackQueryHandler(settings_handler.settings_category_selection_handler, 
                         pattern='^(business|entertainment|general|health|science|sports|technology|cancel_config)$'),
    CallbackQueryHandler(settings_handler.setting_selection_handler, pattern='^.+:.+$'), # Handles "category:key"

    # News Handlers
    # Pattern for news_category_selection_handler (handles selection of a news category like 'sports')
    CallbackQueryHandler(news_handler.news_category_selection_handler, 
                         pattern='^(business|entertainment|general|health|science|sports|technology|cancel)$'), # Subtask specified 'cancel'
    # Pattern for news_selection_handler (handles selection of a specific news item by index)
    # The pattern in bot/main.py was '^(news_[0-9]+|cancel_news_selection)$'
    # The subtask specifies: '^[0-9]+|cancel_selection$'
    # I will use the subtask's specified pattern.
    CallbackQueryHandler(news_handler.news_selection_handler, pattern='^[0-9]+|cancel_selection$'),
]

# Message Handlers
message_handlers = [
    # Settings Handlers (Handles new values for settings)
    # This needs to be specific enough not to clash with unknown_command_handler if it's too broad.
    # Assuming handle_new_value is for specific conversational contexts after a setting is selected.
    # If it's meant to catch any text that's not a command, it should be carefully placed.
    # For now, keeping its original filters.
    MessageHandler(filters.TEXT & ~filters.COMMAND & filters.ChatType.PRIVATE, settings_handler.handle_new_value), # Added CHAT_PRIVATE for safety

    # Utility Handlers (Unknown Command)
    # This should be one of the last message handlers if it's a general fallback.
    # filters.COMMAND ensures it only triggers on things that look like commands but aren't recognized.
    MessageHandler(filters.COMMAND & (~filters.UpdateType.EDITED_MESSAGE), utility_handler.unknown_command_handler),
]

